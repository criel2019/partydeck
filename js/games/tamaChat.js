// ── Chat State ───────────────────────────────────────────
let tamaChatMessages = [];
let tamaChatOpen = false;
let tamaChatLastCategory = '';
let tamaChatLastIds = [];
let tamaChatAutoTimer = null;
let tamaChatTyping = false;
let tamaChatConsecutiveMonologue = 0;
let tamaChatBurstRemaining = 0;
let tamaChatLastUserTime = 0;
let tamaChatEmojiRepeatCount = {};
let tamaChatBurstArmed = false;
let tamaChatBurstPrimed = false;

// ── Init / Load / Save ──────────────────────────────────
function tamaChatInit() {
  const raw = localStorage.getItem(TAMA_CHAT_STORAGE);
  if (raw) {
    try { tamaChatMessages = JSON.parse(raw); } catch(e) { tamaChatMessages = []; }
  }
  const lastUserMsg = [...tamaChatMessages].reverse().find(msg => msg.sender === 'user');
  tamaChatLastUserTime = lastUserMsg ? lastUserMsg.time : 0;
}

function tamaChatSave() {
  // Keep only last N messages
  if (tamaChatMessages.length > TAMA_CHAT_MAX_MSGS) {
    tamaChatMessages = tamaChatMessages.slice(-TAMA_CHAT_MAX_MSGS);
  }
  localStorage.setItem(TAMA_CHAT_STORAGE, JSON.stringify(tamaChatMessages));
}

function tamaChatClear() {
  tamaChatMessages = [];
  tamaChatLastCategory = '';
  tamaChatLastIds = [];
  tamaChatConsecutiveMonologue = 0;
  tamaChatBurstRemaining = 0;
  tamaChatLastUserTime = 0;
  tamaChatEmojiRepeatCount = {};
  tamaChatBurstArmed = false;
  tamaChatBurstPrimed = false;
  tamaChatClearAutoTimer();
  tamaChatSave();
  tamaChatRender();
}

function tamaChatPickOne(pool) {
  if (!Array.isArray(pool) || !pool.length) return null;
  return pool[Math.floor(Math.random() * pool.length)];
}

function tamaChatRandomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function tamaChatTrackCategory(categoryId) {
  tamaChatLastCategory = categoryId || '';
  if (categoryId === 'monologue') tamaChatConsecutiveMonologue += 1;
  else tamaChatConsecutiveMonologue = 0;
}

function tamaChatClearAutoTimer() {
  if (!tamaChatAutoTimer) return;
  clearTimeout(tamaChatAutoTimer);
  tamaChatAutoTimer = null;
}

function tamaChatUserSilenceMs(now) {
  return Math.max(0, now - (tamaChatLastUserTime || now));
}

function tamaChatResolveCategory(pool, fallbackPool) {
  const primary = Array.isArray(pool) ? pool.filter(Boolean) : [];
  const fallback = Array.isArray(fallbackPool) ? fallbackPool.filter(Boolean) : [];

  if (tamaChatConsecutiveMonologue >= 2) {
    const forced = (fallback.length ? fallback : ['idle', 'chitchat'])
      .filter(cat => cat === 'idle' || cat === 'chitchat');
    const forcedAlt = forced.filter(cat => cat !== tamaChatLastCategory);
    return tamaChatPickOne(forcedAlt.length ? forcedAlt : forced);
  }

  const primaryAlt = primary.filter(cat => cat !== tamaChatLastCategory);
  if (primaryAlt.length) return tamaChatPickOne(primaryAlt);

  const fallbackAlt = fallback.filter(cat => cat !== tamaChatLastCategory);
  if (fallbackAlt.length) return tamaChatPickOne(fallbackAlt);

  return tamaChatPickOne(primary.length ? primary : fallback);
}

function tamaChatPickBurstCategory() {
  return tamaChatResolveCategory(
    ['monologue', 'monologue', 'idle', 'chitchat', 'idle'],
    ['idle', 'chitchat']
  );
}

function tamaChatPickReplyPoolText() {
  const mood = typeof tamaMoodInfo === 'function' ? tamaMoodInfo() : null;
  const tribe = (tamaPet && tamaPet.tribe) || 'fire';
  let tribePool = (TAMA_TRIBE_REPLY_POOL[tribe] || []).slice();
  let commonPool = TAMA_REPLY_POOL.slice();
  if (mood && (mood.cls === 'bad' || mood.cls === 'worst')) {
    const blocked = ['최고', '신나', '파이팅', '멋져'];
    const tribeFiltered = tribePool.filter(text => !blocked.some(keyword => text.includes(keyword)));
    const commonFiltered = commonPool.filter(text => !blocked.some(keyword => text.includes(keyword)));
    if (tribeFiltered.length) tribePool = tribeFiltered;
    if (commonFiltered.length) commonPool = commonFiltered;
  }

  if (tribePool.length && Math.random() < 0.72) {
    return tamaChatPickOne(tribePool);
  }
  return tamaChatPickOne(commonPool.length ? commonPool : tribePool);
}

