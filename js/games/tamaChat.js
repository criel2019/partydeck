// ── Render chat UI ──────────────────────────────────────
function tamaChatRender() {
  const container = document.getElementById('tamaChatMsgList');
  if (!container) return;

  let html = '';
  const petName = (tamaPet && tamaPet.name) || '펫';
  const tribe = (tamaPet && tamaPet.tribe) || 'fire';
  const tribeInfo = TAMA_TRIBES[tribe] || TAMA_TRIBES.fire;

  tamaChatMessages.forEach(msg => {
    const timeStr = new Date(msg.time).toLocaleTimeString('ko-KR', { hour:'2-digit', minute:'2-digit' });
    if (msg.sender === 'pet') {
      html += `<div class="tc-msg tc-pet">
        <div class="tc-pet-avatar">${tribeInfo.emoji}</div>
        <div class="tc-bubble-wrap">
          <div class="tc-pet-name">${petName}</div>
          <div class="tc-bubble tc-bubble-pet">${msg.text}</div>
          <div class="tc-time">${timeStr}</div>
        </div>
      </div>`;
    } else {
      html += `<div class="tc-msg tc-user">
        <div class="tc-bubble-wrap">
          <div class="tc-bubble tc-bubble-user ${msg.cls||''}">${msg.text}</div>
          <div class="tc-time">${timeStr}</div>
        </div>
      </div>`;
    }
  });

  container.innerHTML = html;
  tamaChatScrollBottom();
}

function tamaChatScrollBottom() {
  const el = document.getElementById('tamaChatMsgList');
  if (el) requestAnimationFrame(() => { el.scrollTop = el.scrollHeight; });
}

// ── Open / Close Chat ───────────────────────────────────
function tamaChatOpen_() {
  tamaChatOpen = true;
  tamaChatInit();
  const ovl = document.getElementById('tamaChatOverlay');
  if (ovl) ovl.classList.add('active');

  // Update title and typing avatar
  const tribe = (tamaPet && tamaPet.tribe) || 'fire';
  const tribeInfo = TAMA_TRIBES[tribe] || TAMA_TRIBES.fire;
  const petName = (tamaPet && tamaPet.name) || '펫';
  const titleEl = document.getElementById('tamaChatTitle');
  if (titleEl) titleEl.textContent = tribeInfo.emoji + ' ' + petName + '와의 대화';
  const avatarEl = document.getElementById('tamaChatTypingAvatar');
  if (avatarEl) avatarEl.textContent = tribeInfo.emoji;

  tamaChatRender();
  tamaChatRenderEmojis();

  // Send greeting on open
  const h = new Date().getHours();
  let greetCat;
  if (h >= 22 || h < 6) greetCat = 'greet_night';
  else if (h < 12) greetCat = 'greet_morning';
  else greetCat = 'greet_day';

  // Only greet if last message was >5 min ago
  const lastMsg = tamaChatMessages[tamaChatMessages.length - 1];
  if (!lastMsg || Date.now() - lastMsg.time > 300000) {
    setTimeout(() => tamaChatPetSay(greetCat), 500);
  }
}

function tamaChatClose() {
  tamaChatOpen = false;
  const ovl = document.getElementById('tamaChatOverlay');
  if (ovl) ovl.classList.remove('active');
  tamaChatBurstRemaining = 0;
  tamaChatBurstArmed = false;
  tamaChatBurstPrimed = false;
  tamaChatClearAutoTimer();
}

function tamaChatRenderEmojis() {
  const bar = document.getElementById('tamaChatEmojiBar');
  if (!bar) return;
  bar.innerHTML = TAMA_CHAT_EMOJIS.map(e =>
    `<button class="tc-emoji-btn" onclick="tamaChatUserEmoji('${e.id}')" title="${e.label}">
      <span class="tc-emoji-icon">${e.emoji}</span>
      <span class="tc-emoji-label">${e.label}</span>
    </button>`
  ).join('');
}
