function tamaChatPickAutoDelay(silenceMs) {
  if (tamaChatBurstRemaining > 0 && !tamaChatBurstArmed) {
    return tamaChatRandomInt(7000, 20000);
  }
  if (silenceMs >= 300000) {
    return tamaChatRandomInt(14000, 28000);
  }
  if (silenceMs >= 120000) {
    return tamaChatRandomInt(22000, 48000);
  }
  return tamaChatRandomInt(35000, 120000);
}

// ── Core: Pick dialogue entry ───────────────────────────
function tamaChatPick(categoryId) {
  if (!tamaPet) return null;
  const tribe = tamaPet.tribe || 'fire';
  const tribeData = TAMA_CHAT_DATA[tribe];
  if (!tribeData || !tribeData[categoryId]) return null;

  const pool = tribeData[categoryId];
  // Avoid repeating last 3 entries
  const filtered = pool.filter(e => !tamaChatLastIds.includes(e.id));
  const pick = filtered.length > 0
    ? filtered[Math.floor(Math.random() * filtered.length)]
    : pool[Math.floor(Math.random() * pool.length)];

  // Track recent IDs
  tamaChatLastIds.push(pick.id);
  if (tamaChatLastIds.length > 3) tamaChatLastIds.shift();
  tamaChatTrackCategory(categoryId);

  return pick;
}

// ── Core: Add message ───────────────────────────────────
function tamaChatAddMessage(sender, text, extraClass) {
  const msg = {
    sender, // 'pet' or 'user'
    text,
    time: Date.now(),
    cls: extraClass || ''
  };
  tamaChatMessages.push(msg);
  tamaChatSave();
  if (tamaChatOpen) tamaChatRender();
}

// ── Pet sends a message (with typing animation) ─────────
function tamaChatPetSay(categoryId) {
  const entry = tamaChatPick(categoryId);
  if (!entry) return;
  tamaChatShowTyping();
  const delay = 600 + Math.random() * 800;
  setTimeout(() => {
    tamaChatHideTyping();
    tamaChatAddMessage('pet', entry.text);
    tamaChatScrollBottom();
  }, delay);
}

function tamaChatPetSayDirect(text, categoryId) {
  tamaChatTrackCategory(categoryId || 'direct');
  tamaChatShowTyping();
  setTimeout(() => {
    tamaChatHideTyping();
    tamaChatAddMessage('pet', text);
    tamaChatScrollBottom();
  }, 500);
}

// ── User sends emoji reaction ───────────────────────────
function tamaChatUserEmoji(emojiId) {
  const emojiDef = TAMA_CHAT_EMOJIS.find(e => e.id === emojiId);
  if (!emojiDef) return;

  tamaChatLastUserTime = Date.now();
  tamaChatBurstRemaining = 0;
  tamaChatBurstArmed = false;
  tamaChatBurstPrimed = false;
  tamaChatClearAutoTimer();

  Object.keys(tamaChatEmojiRepeatCount).forEach(id => {
    if (id !== emojiId) tamaChatEmojiRepeatCount[id] = 0;
  });
  tamaChatEmojiRepeatCount[emojiId] = (tamaChatEmojiRepeatCount[emojiId] || 0) + 1;

  tamaChatAddMessage('user', emojiDef.emoji + ' ' + emojiDef.label, 'user-emoji');

  const repeatReply = tamaChatPickEmojiRepeatReply(emojiId, tamaChatEmojiRepeatCount[emojiId]);
  if (repeatReply) {
    setTimeout(() => tamaChatPetSayDirect(repeatReply, 'emoji_repeat'), 300);
  } else {
    const replyPoolText = Math.random() < 0.4 ? tamaChatPickReplyPoolText() : '';
    if (replyPoolText) {
      setTimeout(() => tamaChatPetSayDirect(replyPoolText, 'reply_pool'), 300);
    } else {
      setTimeout(() => tamaChatPetSay(emojiDef.cat), 300);
    }
  }

  // Affinity bonus
  if (typeof tamaPet !== 'undefined' && tamaPet) {
    tamaPet.affinity = Math.min((tamaPet.affinity || 0) + 1, 9999);
  }
}

// ── Auto chat (called from tamagotchi tick) ──────────────
function tamaChatAutoTick() {
  if (!tamaPet || !tamaChatOpen) return;
  const silenceMs = tamaChatUserSilenceMs(Date.now());

  if (silenceMs < 22000) {
    tamaChatClearAutoTimer();
    return;
  }

  if (!tamaChatBurstPrimed && tamaChatBurstRemaining <= 0 && silenceMs >= 300000) {
    tamaChatBurstRemaining = tamaChatRandomInt(1, 3);
    tamaChatBurstArmed = true;
    tamaChatBurstPrimed = true;
  }

  if (tamaChatAutoTimer) return;

  tamaChatAutoTimer = setTimeout(() => {
    tamaChatAutoTimer = null;
    if (!tamaChatOpen || !tamaPet) return;

    let cat = '';
    if (tamaChatBurstRemaining > 0 && !tamaChatBurstArmed) {
      cat = tamaChatPickBurstCategory();
      tamaChatBurstRemaining = Math.max(0, tamaChatBurstRemaining - 1);
    } else {
      cat = tamaChatPickAutoCategory();
      if (tamaChatBurstArmed) tamaChatBurstArmed = false;
    }

    if (cat) tamaChatPetSay(cat);
  }, tamaChatPickAutoDelay(silenceMs));
}

function tamaChatPickAutoCategory() {
  if (!tamaPet) return 'idle';
  const n = tamaPet.needs || {};
  const mood = tamaMoodInfo();
  const fallback = ['idle', 'chitchat', 'monologue', 'praise'];

  if ((n.hunger || 0) < 30) {
    return tamaChatResolveCategory(['mood_sad', 'mood_sad', 'idle', 'monologue'], fallback);
  }
  if (mood.cls === 'best' || mood.cls === 'good') {
    return tamaChatResolveCategory(['mood_happy', 'chitchat', 'praise', 'monologue'], fallback);
  }
  if (mood.cls === 'bad' || mood.cls === 'worst') {
    return tamaChatResolveCategory(['mood_sad', 'idle', 'monologue', 'chitchat'], fallback);
  }

  return tamaChatResolveCategory(
    ['idle', 'idle', 'chitchat', 'chitchat', 'monologue', 'monologue', 'praise'],
    fallback
  );
}

